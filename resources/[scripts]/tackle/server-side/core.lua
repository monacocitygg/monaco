local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- TACKLE:UPDATE
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterServerEvent("tackle:Update")
AddEventHandler("tackle:Update",function(targetId)
	local source = source
	if vRP.Passport(source) and vRP.Passport(targetId) then
		TriggerClientEvent("tackle:Player",targetId)
	end
end)