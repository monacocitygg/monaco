-- Carrega os emotes adicionais deste arquivo dentro da tabela global `RP`.
function LoadAddonEmotes()
  local prefixByCategory = {
    Shared = " ",
    AnimalEmotes = " ",
    PropEmotes = " ",
  }

  for categoryName, categoryTable in pairs(AddonEmotes) do
    if RP[categoryName] then
      local prefix = prefixByCategory[categoryName]

      for emoteName, emoteData in pairs(categoryTable) do
        if prefix then
          emoteData[3] = prefix .. emoteData[3]
        end

        RP[categoryName][emoteName] = emoteData
      end
    end
  end

  AddonEmotes = nil
end

